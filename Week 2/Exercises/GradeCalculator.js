
var num = prompt("Enter Your Grade: ");
if (num >= 90) {
    console.log("Your grade is A");
} else if (num >= 80) {
    console.log("Your grade is B");
} else if (num >= 70) {
    console.log("Your grade is C");
} else if (num >= 60) {
    console.log("Your grade is D");
} else {
    console.log("Your grade is F");
} 



    
